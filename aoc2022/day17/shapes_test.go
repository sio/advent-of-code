package main

import (
	"testing"
)

const expected = `
##.....
##.....
.......
#......
#......
#......
#......
.......
..#....
..#....
###....
.......
.#.....
###....
.#.....
.......
####...
`

func TestDefaultShapes(t *testing.T) {
	shapes := DefaultShapes()
	chamber := Chamber{width: 7}
	y := 0
	for _, s := range shapes {
		s.corner.Y = y
		chamber.Settle(&s)
		y += s.height + 1
	}
	got := "\n" + chamber.Render()
	if got != expected {
		t.Errorf("unexpected default shapes:\n%s", got)
	}
}
